//while

function bucleWhile(){

let contador = 10;
let array = [];

while (contador >= 1) {
    
    array.push(contador);

    contador--;
}
    alert(`Los numeros son: ${array.join(" - ")}`);
}

//for

function bucleFor(){
for (let i = 10; i >= 1; i--) {

    array.push(i);
    
    }
    alert(`Los numeros son: ${array.join(" - ")}`);
}

